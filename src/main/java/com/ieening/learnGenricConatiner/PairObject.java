package com.ieening.learnGenricConatiner;

public class PairObject {
    private Object first;
    private Object second;

    public PairObject(Object first, Object second) {
        this.first = first;
        this.second = second;
    }

    public Object getFirst() {
        return first;
    }

    public Object getSecond() {
        return second;
    }
}
